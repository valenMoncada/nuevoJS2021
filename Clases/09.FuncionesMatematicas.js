//Valor absoluto de un número
console.log(Math.abs(-45));

//Potencias (base, exponente)
console.log(Math.pow(3, 2));

//Raiz cuadrada
console.log(Math.sqrt(64));

//Redondear al más cercano
console.log(Math.round(34.6));

//Random
console.log(Math.random());
//Random 0 a 10
console.log(Math.random() * 10);
//Random entre parametros (ejm entre 5 y 10)
console.log(Math.random() * (10 - 5) + 5);

//sign: signo del número. 1 si es positivo, -1 si es negativo
console.log(Math.sign(-33));

//Ceil: redondea al entero superior
console.log(Math.ceil(0.3));

//floor: redondea al entero inferior
console.log(Math.floor(0.9));